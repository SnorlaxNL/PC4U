	function readmoreMw2() {
		var button = document.getElementById('readmore-mw2');				// Al deze functies zorgen ervoor dat de "meer lezen" button werkt op de mobiele versie van de website. Er moesten aparte functies gemaakt worden omdat er aparte id's gebruikt moesten worden.
		var showText = document.getElementById('mw2-description')			// Zodra je op de button klikt wordt er wat CSS veranderd. De button wordt onzichtbaar (display: none) en de tekst zichtbaar (display: block).

		button.style.display = "none";										// Dit herhaalt zich bij alle functies in dit script.
		showText.style.display = "block";
	}
	
